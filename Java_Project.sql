CREATE DATABASE RAILWAY1;
USE RAILWAY1;

CREATE TABLE LOGIN(

USERNAME VARCHAR(50) PRIMARY KEY,
PASSWORD1 VARCHAR(50) 
CHECK(PASSWORD1 <=> 'PASSWORD')
);

CREATE TABLE USER1(
USERNAME VARCHAR(50),
FOREIGN KEY(USERNAME) REFERENCES LOGIN(USERNAME),
FULL_NAME VARCHAR(50),
ADRESS VARCHAR(50),
CITY VARCHAR(50),
CONTACT INT,
AGE INT,
GENDER CHAR(1) CHECK(GENDER = 'M' OR GENDER = 'F')


);

CREATE TABLE STATION(

TRAIN_NO INT PRIMARY KEY,
MUMBAI INT,
AHAMADABAD INT,
JAIPUR INT,
DELHI INT,
END1 VARCHAR(50)

);

CREATE TABLE TRAIN(
TRAIN_NO INT ,
FOREIGN KEY (TRAIN_NO) REFERENCES STATION(TRAIN_NO),
TRAIN_NAME VARCHAR(50) NOT NULL,
SOURCE1 VARCHAR(50) NOT NULL,
DEST1 VARCHAR(50) NOT NULL,
MONDAY INT,
TUESDAY INT,
WEDNESDAY INT,
COST INT

);

CREATE TABLE PASSENGER(

PNR INT PRIMARY KEY,
USERNAME VARCHAR(50),
FOREIGN KEY(USERNAME) REFERENCES LOGIN(USERNAME),
AGE INT,
P_NAME VARCHAR(50),
SOURCE1 VARCHAR(50),
DEST1 VARCHAR(50),
TRAIN_NO INT

);

CREATE TABLE PASSENGERBKP(
PNR INT,
FOREIGN KEY (PNR) REFERENCES PASSENGER(PNR),
USERNAME VARCHAR(50) NOT NULL,
AGE INT NOT NULL,
P_NAME VARCHAR(50) NOT NULL,
GENDER CHAR(1) CHECK(GENDER = 'M' OR GENDER = 'F'),
SOURCE1 VARCHAR(50) NOT NULL,
DEST1 VARCHAR(50) NOT NULL,
TRAIN_NO INT


);

